package com.skuniv.CareServer.model;

public class InfoLogin {
	String parent_id;
	String parent_pw;
	String std_no;
	String state;
	public String getParent_id() {
		return parent_id;
	}
	public void setParent_id(String parent_id) {
		this.parent_id = parent_id;
	}
	public String getParent_pw() {
		return parent_pw;
	}
	public void setParent_pw(String parent_pw) {
		this.parent_pw = parent_pw;
	}
	public String getStd_no() {
		return std_no;
	}
	public void setStd_no(String std_no) {
		this.std_no = std_no;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	

}
